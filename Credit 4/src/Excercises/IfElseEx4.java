public class IfElseEx4 
{

	public static void main(String[] args) 
	{
		int x = 50;
		int y = 50;
		if (x == y) 
		{
		  System.out.println("1");
		} 
		else if (x > y) 
		{
		  System.out.println("2");
		} 
		else
		{
		  System.out.println("3");
		}  

	}

}
